package com.example.jkliew.jk_fms;

public class Error {
    String Parameter, Target_Parameter, Average_Parameter, Time, Date, Result;

    public Error() {
    }

    public String getParameter() {
        return Parameter;
    }

    public void setParameter(String parameter) {
        Parameter = parameter;
    }

    public String getTarget_Parameter() {
        return Target_Parameter;
    }

    public void setTarget_Parameter(String target_Parameter) {
        Target_Parameter = target_Parameter;
    }

    public String getAverage_Parameter() {
        return Average_Parameter;
    }

    public void setAverage_Parameter(String average_Parameter) {
        Average_Parameter = average_Parameter;
    }

    public String getTime() {
        return Time;
    }

    public void setTime(String time) {
        Time = time;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getResult() {
        return Result;
    }

    public void setResult(String result) {
        Result = result;
    }
}


